const stroke_glyph_t *stroke_font_glyph(int ch)
{
    if (ch < 32 || ch > 126) return &glyphs[0];
    return &glyphs[ch - 32];
}

float stroke_font_advance(int ch)
{
    (void)ch;
    return (float)STROKE_GRID_W + 1.0f;
}

float stroke_font_text_width(const char *text, int len, float size)
{
    float scale = size / (float)STROKE_GRID_H;
    float advance = ((float)STROKE_GRID_W + 1.0f) * scale;
    int n = (len < 0) ? (int)strlen(text) : len;
    return (float)n * advance;
}